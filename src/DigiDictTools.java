public class DigiDictTools {
    public static String intToStr(int i) {
        if (i==1) return DigitDict.ONE;
        if (i==2) return DigitDict.TWO;
        if (i==3) return DigitDict.THREE;
        if (i==4) return DigitDict.FOUR;
        if (i==5) return DigitDict.FIVE;
        if (i==6) return DigitDict.SIX;
        if (i==7) return DigitDict.SEVEN;
        if (i==8) return DigitDict.EIGHT;
        if (i==9) return DigitDict.NINE;
        return null;
    }
}
